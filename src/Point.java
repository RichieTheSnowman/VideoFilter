public class Point {
    private int grey, x, y;

    public int getX() {
        return x;
    }


    public int getY() {
        return y;
    }


    public Point(int x, int y) {
        this.x = x;
        this.y = y;

    }

    public int getGrey() {
        return grey;
    }

    public void setGrey(int grey) {
        this.grey = grey;
    }
}
